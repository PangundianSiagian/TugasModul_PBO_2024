package Looping;
/**
 *
 * @author Asus
 */
public class PernyataanDoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println("Bilangan Genap antara 1 sampai 20 menggunakan Pernyataan Do-WHILE:");
        int i = 2;
        do {
            System.out.println("\n"+i+" Adalah Bilangan Genap ");
            i += 2; 
        } while (i <= 20);
        System.out.println();
    }
}
