public class PerkaliandanBagi2 {
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //Memberikan nilai 
        int angka = 10;

        // Perkalian dengan 2
        int Perkalian = angka << 1;

        // Pembagian dengan 2
        int Bagi = angka >> 1;

        System.out.println("Angka awal: " + angka);
        System.out.println("Menampilkan Hasil perkalian dengan 2 : " +Perkalian);
        System.out.println("Menampilkan Hasil pembagian dengan 2 : " +Bagi);
    }
}
    
